    private void btSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btSalvarActionPerformed

        DaoPessoa dP = new DaoPessoa();
        if(dP.estaConectado()){
            Pessoa pessoa = Login.getAnalista().getPessoa();
            String CPF = txtCPF.getText().replace("-","");
            CPF = CPF.replace(".","");
            pessoa.setCPF(CPF);
            pessoa.setDt_nasc(txtDtnascimento.getText());
            pessoa.setNome(txtNome.getText());
            pessoa.setSexo(jComboBox1.getSelectedIndex()+1);
            if(dP.editar(pessoa, dP.getId(this.CPF_antigo))){
                Login.getAnalista().setPessoa(pessoa);
                JOptionPane.showMessageDialog(null,"Seu perfil foi atualizado!","SUCESSO", 3);
            }else{
                JOptionPane.showMessageDialog(null,"Ocorreu um falha ao tentar alterar seu perfil!","FALHA", 3);
            }
        }else{
            JOptionPane.showMessageDialog(null,"Nenhuma conex√£o detectada","FALHA", 3);
        }
    }//GEN-LAST:event_btSalvarActionPerformed


